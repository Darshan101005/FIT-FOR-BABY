export const getFoodsByCategory = (category: string): FoodItemData[] => {
  return foodDatabase.filter(food => food.category === category);
};

export const searchFoods = (query: string): FoodItemData[] => {
  const lowerQuery = query.toLowerCase();
  return foodDatabase.filter(
    food => 
      food.name.toLowerCase().includes(lowerQuery) ||
      food.nameTamil.includes(query) ||
      food.subCategory.toLowerCase().includes(lowerQuery)
  );
};

export const calculateNutrition = (
  food: FoodItemData, 
  grams: number
): { calories: number; protein: number; carbs: number; fat: number } => {
  const multiplier = grams / 100;
  return {
    calories: Math.round(food.caloriesPer100g * multiplier),
    protein: Math.round(food.proteinPer100g * multiplier * 10) / 10,
    carbs: Math.round(food.carbsPer100g * multiplier * 10) / 10,
    fat: Math.round(food.fatPer100g * multiplier * 10) / 10,
  };
};
